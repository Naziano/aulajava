public class EstruturaIf {

    public static void main(String[] args) {

        double media = 6.0;

        if (media >= 7) {
            System.out.println("APROVADO");
        } else  if (media < 3) {
            System.out.println("REPROVADO");
        } else {
            System.out.println("RECUPERAÇÂO");
        }


    }

}
